<?php

namespace DevHelper\Plugin\DataVisualizer;

class DataObject
{
    protected string $data = "";

    /**
     * @return string
     */
    public function getData(): string
    {
        return $this->data;
    }

    /**
     * @param string $data
     */
    public function setData(string $data): void
    {
        $this->data = $data;
    }


}
